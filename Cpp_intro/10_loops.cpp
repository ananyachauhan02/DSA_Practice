#include<iostream>

using namespace std;

int main(){
    cout<<"FOR LOOP"<<endl;
int n = 5;

    // FOR LOOP 

    for(int i =0; i<n; i++){
        cout<<i<<endl;
    }
    cout<<"WHILE LOOP"<<endl;

    // WHILE LOOP
    int j = 5;
    while(j <= 9){
        cout<<j<<endl;
        j++;
    }

    // DO WHILE LOOP
    
    return 0;
}

